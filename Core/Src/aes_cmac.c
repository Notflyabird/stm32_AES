// 左移16字节块（处理溢出）
static void left_shift_block(const uint8_t *in, uint8_t *out) {
    uint8_t overflow = 0;
    for (int i = AES_BLOCK_SIZE - 1; i >= 0; i--) {
        out[i] = (in[i] << 1) | overflow;
        overflow = (in[i] & 0x80) ? 1 : 0;
    }
}

// 16字节块异或
static void xor_block(const uint8_t *a, const uint8_t *b, uint8_t *out) {
    for (int i = 0; i < AES_BLOCK_SIZE; i++) {
        out[i] = a[i] ^ b[i];
    }
}

// 串口打印十六进制（辅助调试）
static void uart_print_hex(const uint8_t *data, uint32_t len, const char *prefix) {
    HAL_UART_Transmit(&huart1, (uint8_t*)prefix, strlen(prefix), 0xFFFF);
    for (uint32_t i = 0; i < len; i++) {
        uint8_t hex[3];
        sprintf((char*)hex, "%02X ", data[i]);
        HAL_UART_Transmit(&huart1, hex, 3, 0xFFFF);
    }
    HAL_UART_Transmit(&huart1, (uint8_t*)"\r\n", 2, 0xFFFF);
}

